package com.blueiobase.api.android.bannerx.baseindicator.enums

/**
 * Constants representing the horizontal location of the `BannerX` indicator widget.
 * @author IODevBlue
 * @since 1.0.0
 */
internal enum class IndicatorHorizontalArrangement {
    /** Align indicator widget at the start position of `BannerX`. */
    START,
    /** Align indicator widget at the center of `BannerX`. */
    CENTER,
    /** Align indicator widget at the end of `BannerX`. */
    END
}